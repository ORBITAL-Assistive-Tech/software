def brf_to_binary(filename):
    with open(filename, "r") as f:
        brf_text = f.read()

    braille = []
    line_braille = []
    for i in brf_text.upper():
        if i == "\n":
            braille.append(line_braille)
            line_braille = []
            continue
        line_braille.append(conversion[i])

    return (brf_text, braille)
